	void Map::AddTile(float x, float y) {
		auto MapTile = gCoordinator.CreateEntity();
		float xPos = x * mTileSize.x * mScale.x;
		float yPos = y * mTileSize.y * mScale.y;
		gCoordinator.AddComponent(
			MapTile,
			ECS::Transform{
				sf::Vector2f(xPos, yPos),
				sf::Vector2f(0.f, 0.f),
				mScale
			});

		sf::Texture texture;
		texture = gTextureLoader.getTexture(mTextureId);
		sf::Sprite sprite;
		gCoordinator.AddComponent(
			MapTile,
			ECS::Sprite{
				sprite,
				texture,
				sf::IntRect(272, 288, mTileSize.x, mTileSize.y)
			});
		gCoordinator.GetComponent<ECS::Sprite>(MapTile).init();

		gCoordinator.AddComponent(
			MapTile,
			ECS::Collision{
				sf::FloatRect(
					xPos, 
					yPos, 
					static_cast<float>(mTileSize.x), 
					static_cast<float>(mTileSize.y)
				),
				"GroundTile"
			});
	}

